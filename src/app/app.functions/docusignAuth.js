/**
 * Generate JWT token with extensive debug logging
 */
function generateJWT(integrationKey, userId, privateKey, audience) {
  console.log('üîß Starting JWT generation...');
  console.log('üîß Integration Key:', integrationKey);
  console.log('üîß User ID:', userId);
  console.log('üîß Audience:', audience);
  console.log('üîß Private Key Length:', privateKey.length);
  console.log('üîß Private Key Preview:', privateKey.substring(0, 50) + '...');
  
  try {
    const now = Math.floor(Date.now() / 1000);
    console.log('üîß Current timestamp:', now);
    
    const header = {
      alg: 'RS256',
      typ: 'JWT'
    };
    console.log('üîß JWT Header:', JSON.stringify(header));

    const payload = {
      iss: integrationKey,
      sub: userId,
      iat: now,
      exp: now + 3600,
      aud: audience,
      scope: 'signature impersonation extended'
    };
    console.log('üîç JWT Payload:', JSON.stringify(payload, null, 2));

    // Step 1: Encode header
    console.log('üîß Step 1: Encoding header...');
    let encodedHeader;
    try {
      const headerString = JSON.stringify(header);
      console.log('üîß Header string:', headerString);
      const headerBuffer = Buffer.from(headerString);
      console.log('üîß Header buffer length:', headerBuffer.length);
      const headerBase64 = headerBuffer.toString('base64');
      console.log('üîß Header base64:', headerBase64);
      encodedHeader = headerBase64
        .replace(/\+/g, '-')
        .replace(/\//g, '_')
        .replace(/=+$/, '');
      console.log('üîß Encoded header:', encodedHeader);
    } catch (headerError) {
      console.error('‚ùå Header encoding error:', headerError);
      console.error('‚ùå Header error stack:', headerError.stack);
      throw new Error(`Header encoding failed: ${headerError.message}`);
    }

    // Step 2: Encode payload
    console.log('üîß Step 2: Encoding payload...');
    let encodedPayload;
    try {
      const payloadString = JSON.stringify(payload);
      console.log('üîß Payload string:', payloadString);
      const payloadBuffer = Buffer.from(payloadString);
      console.log('üîß Payload buffer length:', payloadBuffer.length);
      const payloadBase64 = payloadBuffer.toString('base64');
      console.log('üîß Payload base64:', payloadBase64);
      encodedPayload = payloadBase64
        .replace(/\+/g, '-')
        .replace(/\//g, '_')
        .replace(/=+$/, '');
      console.log('üîß Encoded payload:', encodedPayload);
    } catch (payloadError) {
      console.error('‚ùå Payload encoding error:', payloadError);
      console.error('‚ùå Payload error stack:', payloadError.stack);
      throw new Error(`Payload encoding failed: ${payloadError.message}`);
    }

    // Step 3: Create signature base
    console.log('üîß Step 3: Creating signature base...');
    const signatureBase = `${encodedHeader}.${encodedPayload}`;
    console.log('üîß Signature base length:', signatureBase.length);
    console.log('üîß Signature base preview:', signatureBase.substring(0, 100) + '...');

    // Step 4: Test private key
    console.log('üîß Step 4: Testing private key format...');
    try {
      const testSigner = crypto.createSign('RSA-SHA256');
      testSigner.update('test-data');
      const testSignature = testSigner.sign(privateKey, 'base64');
      console.log('üîß Private key test successful, signature length:', testSignature.length);
    } catch (keyTestError) {
      console.error('‚ùå Private key test failed:', keyTestError);
      console.error('‚ùå Key test error stack:', keyTestError.stack);
      console.error('‚ùå Key test error code:', keyTestError.code);
      
      // Try to convert the key
      console.log('üîß Attempting key conversion...');
      try {
        const keyObject = crypto.createPrivateKey(privateKey);
        console.log('üîß Key object created successfully');
        const convertedKey = keyObject.export({
          format: 'pem',
          type: 'pkcs8'
        });
        console.log('üîß Key converted to PKCS8 format');
        privateKey = convertedKey; // Use converted key
      } catch (conversionError) {
        console.error('‚ùå Key conversion failed:', conversionError);
        console.error('‚ùå Conversion error stack:', conversionError.stack);
        throw new Error(`Private key is invalid and cannot be converted: ${conversionError.message}`);
      }
    }

    // Step 5: Create signature
    console.log('üîß Step 5: Creating signature...');
    let signature;
    try {
      const signer = crypto.createSign('RSA-SHA256');
      console.log('üîß Signer created');
      signer.update(signatureBase);
      console.log('üîß Signature base updated');
      const rawSignature = signer.sign(privateKey, 'base64');
      console.log('üîß Raw signature created, length:', rawSignature.length);
      console.log('üîß Raw signature preview:', rawSignature.substring(0, 50) + '...');
      signature = rawSignature
        .replace(/\+/g, '-')
        .replace(/\//g, '_')
        .replace(/=+$/, '');
      console.log('üîß URL-safe signature created, length:', signature.length);
    } catch (signatureError) {
      console.error('‚ùå Signature creation error:', signatureError);
      console.error('‚ùå Signature error stack:', signatureError.stack);
      console.error('‚ùå Signature error code:', signatureError.code);
      console.error('‚ùå Signature error type:', typeof signatureError);
      console.error('‚ùå Signature error details:', Object.keys(signatureError));
      throw new Error(`Signature creation failed: ${signatureError.message}`);
    }

    // Step 6: Assemble final token
    console.log('üîß Step 6: Assembling final token...');
    const finalToken = `${signatureBase}.${signature}`;
    console.log('üîß Final JWT token length:', finalToken.length);
    console.log('üîß Final JWT token preview:', finalToken.substring(0, 100) + '...');
    
    // Validate token format
    const parts = finalToken.split('.');
    console.log('üîß Token parts count:', parts.length);
    console.log('üîß Header part length:', parts[0]?.length);
    console.log('üîß Payload part length:', parts[1]?.length);
    console.log('üîß Signature part length:', parts[2]?.length);
    
    if (parts.length !== 3) {
      throw new Error(`Invalid JWT format - expected 3 parts, got ${parts.length}`);
    }
    
    console.log('‚úÖ JWT generation completed successfully');
    return finalToken;
    
  } catch (jwtError) {
    console.error('‚ùå JWT generation error:', jwtError);
    console.error('‚ùå JWT error message:', jwtError.message);
    console.error('‚ùå JWT error stack:', jwtError.stack);
    console.error('‚ùå JWT error code:', jwtError.code);
    console.error('‚ùå JWT error type:', typeof jwtError);
    console.error('‚ùå JWT error constructor:', jwtError.constructor.name);
    console.error('‚ùå JWT error details:', JSON.stringify(jwtError, Object.getOwnPropertyNames(jwtError), 2));
    throw jwtError;
  }
}